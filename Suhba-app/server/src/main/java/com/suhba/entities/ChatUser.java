public class ChatUser {
    private int userId;
    private long chatId;

    // Getters
    public int getUserId() { return userId; }
    public long getChatId() { return chatId; }

    // Setters
    public void setUserId(int userId) { this.userId = userId; }
    public void setChatId(long chatId) { this.chatId = chatId; }
}